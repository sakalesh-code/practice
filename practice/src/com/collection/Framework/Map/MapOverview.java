package com.collection.Framework.Map;

public class MapOverview {
	// Map stores data as key → value pairs.
	// each keys must be unique, value can repeat
	// each key maps to one value

	/* Map Comparison Table (Important) */
//	| Feature            | HashMap    | LinkedHashMap            | TreeMap        | Hashtable   | ConcurrentHashMap    |
//	| ------------------ | ---------- | ------------------------ | -------------- | ----------- | -------------------- |
//	| Order              | No order   | Insertion order          | Sorted order   | No order    | No order             |
//	| Null Key           | 1 allowed  | 1 allowed                | Not allowed    | Not allowed | Not allowed          |
//	| Null Value         | Allowed    | Allowed                  | Allowed        | Not allowed | Not allowed          |
//	| Thread Safe        | No         | No                       | No             | Yes         | Yes                  |
//	| Performance        | Fastest    | Slightly slower          | Slower         | Slow        | Fast in multi-thread |
//	| Internal Structure | Hash Table | Hash Table + Linked List | Red-Black Tree | Hash Table  | Segmented Hash Table |
//	| Introduced         | Java 1.2   | Java 1.4                 | Java 1.2       | Java 1.0    | Java 1.5             |

	/* Common Map Methods */
//	| Method               | Purpose             |
//	| -------------------- | ------------------- |
//	| put(key,value)       | Insert data         |
//	| get(key)             | Retrieve value      |
//	| remove(key)          | Delete entry        |
//	| containsKey(key)     | Check key           |
//	| containsValue(value) | Check value         |
//	| keySet()             | Get all keys        |
//	| values()             | Get all values      |
//	| entrySet()           | Get key-value pairs |

}
